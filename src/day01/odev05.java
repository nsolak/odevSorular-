package day01;

public class odev05 {

	public static void main(String[] args) {
		/*
		Iki degisken atayin : 
		num1=1,
		num2=1 
		‘increment’ yontemini kullanarak Carpim Tablosunu yazdirin.
		Note: ++ increment isareti 1 arttirir. 
		Asagidaki sekilde Carpim Tablosunu yazdirin:
		1 X 1 = 1
		1 X 2 = 2  
        */
           int num1=1;
           int num2=1;
        	System.out.println("1*1= "+num1*num2);	   
        	System.out.println("1*2= "+num1*++num2);
        	System.out.println("1*3= "+num1*++num2);	   
        	System.out.println("1*4= "+num1*++num2);
        	System.out.println("1*5= "+num1*++num2);	   
        	System.out.println("1*6= "+num1*++num2);
        	System.out.println("1*7= "+num1*++num2);	   
        	System.out.println("1*8= "+num1*++num2);
        	System.out.println("1*9= "+num1*++num2);
        	System.out.println( "1*10="+num1*++num2);
        	
	}

}
